import { readFile, writeFile } from "node:fs/promises";
import { execFileSync } from "node:child_process";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const catalogPath = join(root, "config/x-credits-catalog.json");
const goPath = join(root, "api/internal/xcredits/catalog_generated.go");
const tsPath = join(root, "dashboard/src/data/x-credits-catalog.generated.ts");
const check = process.argv.includes("--check");

const catalog = JSON.parse(await readFile(catalogPath, "utf8"));
const operationByKey = new Map(catalog.operations.map((operation) => [operation.key, operation]));
const creditFor = (key) => {
  const operation = operationByKey.get(key);
  if (!operation) throw new Error(`unknown capacity operation ${key}`);
  return operation.credits;
};

const capacityFor = (plan) => {
  if (plan.monthly_allowance === null) return null;
  const allowance = plan.monthly_allowance;
  const examples = catalog.capacity_examples;
  return {
    normal_posts: Math.floor(allowance / creditFor(examples.normal_post_operation)),
    url_posts: Math.floor(allowance / creditFor(examples.url_post_operation)),
    comment_interactions: Math.floor(
      allowance /
        (creditFor(examples.comment_receive_operation) +
          creditFor(examples.comment_send_operation)),
    ),
    dm_interactions: Math.floor(
      allowance /
        (creditFor(examples.dm_receive_operation) +
          creditFor(examples.dm_send_operation)),
    ),
  };
};

const goString = (value) => JSON.stringify(value);
const goPlans = catalog.plans
  .filter((plan) => plan.monthly_allowance !== null)
  .map((plan) => `\t${goString(plan.id)}: ${plan.monthly_allowance},`)
  .join("\n");
const goInbound = catalog.plans
  .filter((plan) => plan.inbound_daily_limit !== null)
  .map((plan) => `\t${goString(plan.id)}: ${plan.inbound_daily_limit},`)
  .join("\n");
const goOperations = catalog.operations
  .map(
    (operation) =>
      `\t${goString(operation.key)}: {Label: ${goString(operation.label)}, Credits: ${operation.credits}, LaterPhase: ${operation.phase === "later"}},`,
  )
  .join("\n");

const rawGoOutput = `// Code generated by scripts/generate-x-credits-catalog.mjs; DO NOT EDIT.
package xcredits

const CatalogVersion = ${goString(catalog.version)}

type OperationDefinition struct {
\tLabel      string
\tCredits    int64
\tLaterPhase bool
}

type Capacity struct {
\tNormalPosts         int64
\tURLPosts            int64
\tCommentInteractions int64
\tDMInteractions      int64
}

var planAllowances = map[string]int64{
${goPlans}
}

var inboundDailyLimits = map[string]int64{
${goInbound}
}

var OperationCatalog = map[string]OperationDefinition{
${goOperations}
}

func PlanAllowance(planID string) (int64, bool) {
\tvalue, ok := planAllowances[planID]
\treturn value, ok
}

func InboundDailyLimit(planID string) (int64, bool) {
\tvalue, ok := inboundDailyLimits[planID]
\treturn value, ok
}

func OperationWeight(operationKey string) int64 {
\treturn OperationCatalog[operationKey].Credits
}

func PlanCapacity(planID string) (Capacity, bool) {
\tallowance, ok := PlanAllowance(planID)
\tif !ok {
\t\treturn Capacity{}, false
\t}
\treturn Capacity{
\t\tNormalPosts: allowance / OperationWeight("post.create"),
\t\tURLPosts: allowance / OperationWeight("post.create_url"),
\t\tCommentInteractions: allowance / (OperationWeight("post.read") + OperationWeight("post.create")),
\t\tDMInteractions: allowance / (OperationWeight("dm.read") + OperationWeight("dm.send")),
\t}, true
}
`;
const goOutput = execFileSync("gofmt", [], {
  input: rawGoOutput,
  encoding: "utf8",
});

const generatedPlans = catalog.plans.map((plan) => ({
  ...plan,
  capacity: capacityFor(plan),
}));
const tsOutput = `// Code generated by scripts/generate-x-credits-catalog.mjs; DO NOT EDIT.

export const X_CREDITS_CATALOG_VERSION = ${JSON.stringify(catalog.version)} as const;

export const X_CREDIT_PLANS = ${JSON.stringify(generatedPlans, null, 2)} as const;

export const X_CREDIT_OPERATIONS = ${JSON.stringify(catalog.operations, null, 2)} as const;

export type XCreditPlan = (typeof X_CREDIT_PLANS)[number];
export type XCreditOperation = (typeof X_CREDIT_OPERATIONS)[number];
`;

async function emit(path, output) {
  if (check) {
    const existing = await readFile(path, "utf8").catch(() => "");
    if (existing !== output) {
      throw new Error(`${path.replace(`${root}/`, "")} is out of date`);
    }
    return;
  }
  await writeFile(path, output);
}

await Promise.all([emit(goPath, goOutput), emit(tsPath, tsOutput)]);
